//Conditional statement if,else ,elseif statement

let age="abc"

//if statement

if (age>=18);{
    console.log("Eligible to vote")
}

//else statement

if(age>=18)
{
    console.log("Eligible to vote")
}
else{
console.log("Not eligible to vote")
}

//elseif statement

if (age>=18)
{
    console.log("Eligible to vote")
}
else if(age<=18){
    console.log("Not eligible to vote")
}
else{
    console.log("Invalid data")
}
